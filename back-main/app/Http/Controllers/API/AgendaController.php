     * @return \Illuminate\Http\Response
     */
    public function index()
    {

        try {

            $all = DB::table('agenda AS A')
                ->join('lead AS L','A.id_lead','=','L.id')
                ->selectRaw('A.*, L.nome')
                ->get();

            $events = [];
            foreach ($all as $event) {
                $events[] = ['id'=>$event->id,'title'=>$event->nome,'start'=>$event->data_retorno,'end'=>$event->data_retorno,'category'=>'Teste'];
            }

            return response()->json($events,200);

        }catch (\Exception $e){
            return response()->json(['message'=>$e->getMessage(),'status'=>'error'],400);
        }

    }

